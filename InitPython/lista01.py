a = ["Juan", "Carlos", "Ernesto", "Henry"]

# Imprimir el valor de una posición especifica de la lista A
print(a[0])

# Agregar un valor final a la lista A
a.append("Marcos")

# Insertar un valor en la posición indicada de la lista A
a.insert(4, "Nicole")

# Agregar valores a la lista A
a.extend(["John", "Raul", "Benjamin",])

# Remover valor de la lista A
a.remove("Juan")

# Ver desde la posición indicada hasta el final de la lista A
b = a[2:]
c = a[1:5]

print(a)
print(b)
print(c)

